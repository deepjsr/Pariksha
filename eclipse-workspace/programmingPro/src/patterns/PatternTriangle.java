package patterns;

public class PatternTriangle {

	public static void main(String[] args) {
		int star= 4,space=0;
		for (int i = 0; i <=space; i++) {
			if(star==0)
						{
						System.out.println("*");
						}
			else
				{
				System.out.println(" ");
				star--;
				space++;
				
				}
			
		}
	}

}
